void USART1_IRQHandler(void)		//USART1中断处理函数
{	
	u8 Res;
	//STM_EVAL_LEDToggle(LED2);
	if(USART_GetITStatus(USART1, USART_IT_RXNE) != RESET)  //接收中断(接收到的数据必须是0x0d 0x0a结尾)
	{
		Res =USART_ReceiveData(USART1);//(USART1->DR);	//读取接收到的数据
		
		if((USART1_RX_STA&0x0200)==0)//接收未完成
		{
			if(USART1_RX_STA&0x0100)//接收到了0x0d
			{
				if(Res!=0x0a)USART1_RX_STA=0;//接收错误,重新开始
				else USART1_RX_STA|=0x0200;	//接收完成了 
			}
			else //还没收到0X0D
			{	
				if(Res==0x0d)USART1_RX_STA|=0x0100;
				else
				{
					USART1_RX_BUF[USART1_RX_STA&0X00FF]=Res ;
					USART1_RX_STA++;
					if(USART1_RX_STA==255)
					{
				       flag=1;
					   USART1_RX_STA=0;//接收数据错误,重新开始接收	  
					}
				}		 
			}
		}  		 
	}
}
